#include "ft_printf.h"

int cout_digit(long i)
{
    int j;

    j = 0;
    if (i < 0)
        i = -i;
    while (i > 0)
    {
        i = i / 10;
        j++;
    }
    if (j == 0)
        return(1);
    return (j);
}